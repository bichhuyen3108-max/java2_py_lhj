package ex_object;

public class PersonDTO {

	private final String name;
	private final int age;
	public PersonDTO(String name, int age) {
		this.age = age;
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}
	
	@Override
	public int hashCode() {
		return hashCode();
	}
	
	@Override
	public String toString() {
		
		return toString();
	}
}
